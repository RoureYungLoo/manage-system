select s.*, c.name
from tb_stu s
         left join tb_clazz c on s.clazz_id = c.id;


select case gender
           when 1 then '男性员工'
           when 2 then '女性员工'
    end              as 'name'
     , count(gender) as value
from tb_emp e
group by gender;

select case e.job
           when 1 then '班主任'
           when 2 then '讲师'
           when 3 then '学工主管'
           when 4 then '教研主管'
           when 5 then '咨询师' end as 'jobname',
       count(job)                   as 'count'
from tb_emp e
group by e.job;

SELECT CASE e.job
           WHEN 1 THEN '班主任'
           WHEN 2 THEN '讲师'
           WHEN 3 THEN '学工主管'
           WHEN 4 THEN '教研主管'
           WHEN 5 THEN '咨询师' END AS 'jobName',
       COUNT(job)                   AS 'count'
FROM tb_emp e
GROUP BY e.job;

select case s.degree
           when 1 then '初中'
           when 2 then '高中'
           when 3 then '大专'
           when 4 then '本科'
           when 5 then '硕士'
           when 6 then '博士'
           end         as 'name',
       count(s.degree) as 'value'

from tb_stu s
group by s.degree;

select c.name as 'name', count(s.clazz_id) as 'value'
from tb_stu s
         left join tb_clazz c on s.clazz_id = c.id
group by s.clazz_id;